package net.cf.form.engine.object;

import java.util.List;

/**
 * 模型的字段
 *
 * @author clouds
 */
public interface XField<P extends XFieldProperty> {

    /**
     * 获取字段的编号
     *
     * @return
     */
    String getCode();

    /**
     * 获取字段的名称
     *
     * @return
     */
    String getName();

    /**
     * 获取字段的列名
     *
     * @return
     */
    String getColumnName();

    default boolean isPrimary() {
        return false;
    }

    /**
     * 是否自动生成的主键
     *
     * @return
     */
    default boolean isAutoGeneratedPrimary() {
        return false;
    }

    /**
     * 是否是集合
     *
     * @return
     */
    boolean isCollection();

    /**
     * 获取数据类型
     *
     * @return
     */
    DataType getDataType();

    /**
     * 获取数据的长度
     *
     * @return
     */
    int getDataLength();

    /**
     * 获取数据的精度
     *
     * @return
     */
    int getDataPrecision();

    /**
     * 获取字段的子属性
     *
     * @return
     */
    List<P> getProperties();

    /**
     * 获取默认值
     *
     * @return
     */
    Object getDefaultValue();

}
