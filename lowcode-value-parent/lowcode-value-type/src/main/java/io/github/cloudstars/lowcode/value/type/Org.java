package io.github.cloudstars.lowcode.value.type;

/**
 * 机构数据
 *
 * @author clouds 
 */
public class Org {

    /**
     * 机构编号
     */
    private String key;

    /**
     * 机构名称
     */
    private String name;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
