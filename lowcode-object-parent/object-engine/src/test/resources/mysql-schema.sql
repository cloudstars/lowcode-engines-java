-- 创建出差表
CREATE TABLE TRAVEL
(
    `APPLY_ID`    VARCHAR(36)  NOT NULL,
    `APPLY_NAME`  VARCHAR(64)  NOT NULL,
    `START_DATE`  DATE         NULL,
    `END_DATE`    DATE         NULL,
    `REASON`      VARCHAR(200) NULL DEFAULT '',
    `ATTACH_NAMES` VARCHAR(2000) NULL,
    `ATTACH_KEYS`  VARCHAR(2000) NULL,
    `STATUS`      VARCHAR(10)  NULL DEFAULT '',
    `CREATOR_NAME` VARCHAR(32)  NULL,
    `CREATOR_KEY`  VARCHAR(20)  NULL,
    `CREATED_AT`  DATETIME     NULL,
    `MODIFIER_NAME` VARCHAR(32)  NULL,
    `MODIFIER_KEY` VARCHAR(20)  NULL,
    `MODIFIED_AT` DATETIME     NULL,
    `OWNER_KEY`    VARCHAR(20)  NULL,
     REVISION     INT,
     `DELETED`    TINYINT,
    PRIMARY KEY (`APPLY_ID`)
);

-- 创建出差行程表
CREATE TABLE TRAVEL_TRIP
(
    `TRIP_ID`      INT NOT NULL AUTO_INCREMENT,
    `TRAVEL_APPLY_ID` VARCHAR(36)  NOT NULL,
    `FROM_ADDR`     VARCHAR(100) NOT NULL,
    `TO_ADDR`       VARCHAR(100) NOT NULL,
    `CREATOR_NAME` VARCHAR(32)  NULL,
    `CREATOR_KEY`  VARCHAR(20)  NULL,
    `CREATED_AT`    DATETIME     NULL,
    `MODIFIER_NAME` VARCHAR(32)  NULL,
    `MODIFIER_KEY`  VARCHAR(20)  NULL,
    `MODIFIED_AT`   DATETIME     NULL,
    PRIMARY KEY (`TRIP_ID`)
);


-- 创建员工表
CREATE TABLE STAFF
(
    `STA_ID`       INT NOT NULL AUTO_INCREMENT,
    `STA_CODE`     VARCHAR(20)  NOT NULL,
    `STA_NAME`     VARCHAR(32)  NOT NULL,
    `JOIN_DATE`    DATE,
    `DEPART_DATE`  DATE,
    `DESCR`        VARCHAR(200),
    `STATUS`       VARCHAR(10),
    `HOBBIES`      json  NULL,
    `AGE`          INT  NULL,
    `OWNER_KEY`    VARCHAR(20)  NULL,
    `REVISION`     INT  NULL,
    `DELETED`      TINYINT  NULL,
    `CREATOR_NAME` VARCHAR(32)  NULL,
    `CREATOR_KEY`  VARCHAR(20)  NULL,
    `CREATED_AT`    DATETIME     NULL,
    `MODIFIER_NAME` VARCHAR(32)  NULL,
    `MODIFIER_KEY`  VARCHAR(20)  NULL,
    `MODIFIED_AT`   DATETIME     NULL,
    PRIMARY KEY (`STA_ID`)
);

-- 创建兴趣爱好表
CREATE TABLE HOBBY
(
    `RECORD_ID`     VARCHAR(36) NOT NULL,
    `CODE`          VARCHAR(10) NOT NULL,
    `NAME`          VARCHAR(32) NOT NULL,
    `DESCR`         VARCHAR(1024) NULL,
    `CREATOR_NAME`  VARCHAR(32) NULL,
    `CREATOR_KEY`   VARCHAR(20) NULL,
    `CREATED_AT`    DATETIME NULL,
    `MODIFIER_NAME` VARCHAR(32) NULL,
    `MODIFIER_KEY`  VARCHAR(20) NULL,
    `MODIFIED_AT`   DATETIME NULL,
    PRIMARY KEY (`RECORD_ID`)
);


